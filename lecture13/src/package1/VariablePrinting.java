package package1;

public class VariablePrinting {
    public static void main(String[] args) {
		int A = 8;
	    float B = 9.0f;
	    
	    System.out.println("A");
	    System.out.println("B");
	    System.out.println(A);
	    System.out.println(B);
	    System.out.println(A+B);
	    System.out.println("A+B");
	}
}
