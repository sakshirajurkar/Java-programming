package package1;

public class IfElse {

	public static void main(String[] args) {
		int num = 7;
		if (num==1) {
			System.out.println("one");
		}
		else if (num==2) {
			System.out.println("Two");
		}
		else if (num==3) {
			System.out.println("Three");
		}
		else {
			System.out.println("Wrong input given");
		}
			
	}

}
