package package1;

public class TypesOfValue {

	public static void main(String[] args) {
		int num ;
		float b;
		char c;
		String s;
		//integer 
		num = 45;
		//float
		b = 6.65f;
		//char 
		c = 'S';
		//String
		s = "Hello java";
		System.out.println("value of num "+ num);
		System.out.println("value of b "+b);
		System.out.println("value of c "+c);
		System.out.println("value of s " + s);
	}

}
