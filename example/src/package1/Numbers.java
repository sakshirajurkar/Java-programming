package package1;

public class Numbers {

	public static void main(String[] args) {
		int input = 10;
		if (input > 0) {
			System.out.println("Number is positive");
		}
		else if (input < 0)
		{
			System.out.println("Number is negative");
		}
		else
		{
			System.out.println("Number is zero");
		}
	}

}
