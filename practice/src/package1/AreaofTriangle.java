package package1;

public class AreaofTriangle {
        public static void main(String[] args) {
			int base = 5;
			int height = 7;
			float area = base*height*0.5f;
			System.out.println("area of triangle:"+area);
			
			int a = 5;
			int b = 9;
			int c = 2;
			int perimeter = a+b+c;
			System.out.println("perimeter of triangle:"+perimeter);
		}
}
