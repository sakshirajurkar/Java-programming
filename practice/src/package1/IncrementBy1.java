package package1;

public class IncrementBy1 {
   public static void main(String[] args) {
	int a = 2;
	
	a= a+1;
	System.out.println(a);
	
	a++;
	System.out.println(a);
	
	++a;
	System.out.println(a);
	
	a = 1+a;
	System.out.println(a);
	
	a++;
	System.out.println(a);
	
	a++;
	System.out.println(a);
	
}
}
