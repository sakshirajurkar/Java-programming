package package1;

public class ValuePrint {
    public static void main(String[] args) {
		int num1 = 9;
		float num2 = 87.09f;
		char num3 = 'a';
		long num4 = 76578565;
		short num5 = 8765;
		boolean num6 = true;
		
		System.out.println("value of int:"+num1);
		System.out.println("value of float:"+num2);
		System.out.println("value of char"+num3);
		System.out.println("value of long"+num4);
		System.out.println("value of short"+num5);
		System.out.println("value of boolean "+num6);
		
	}
}
