package package1;

public class SwitchStatement2 {

	public static void main(String[] args) {
		int brother = 4;
		
		switch (brother)
		{
		case 1 : System.out.println("Pritam");break;
		case 2 : System.out.println("Aryan");break;
		case 3 : System.out.println("Shiva");break;
		case 4 : System.out.println("Devansh");break;
		case 5 : System.out.println("Aditya");break;
		case 6 : System.out.println("Vedant");break;
		case 7 : System.out.println("Vaibhav");break;
		default : System.out.println("Sister");break;
		}
		
	}

}
