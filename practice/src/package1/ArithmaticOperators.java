package package1;

public class ArithmaticOperators {
     public static void main(String[] args) {
		int A = 8;
		int B = 5;
		
		System.out.println("Value of A:"+A);
		System.out.println("Value of B:"+B);
		
		//Addition
		int sum = A+B;
		System.out.println("Sum of num="+sum);
		//Substraction
		int subtract = A-B;
		System.out.println("Substraction of num="+subtract);
		//Divsion
		int div = A/B;
		System.out.println("Division of num="+div);
		//Multiplication
		int multiplication = A*B;
		System.out.println("Multiplication of num="+multiplication);
		//reminder
		int reminder =  A % B;
		System.out.println("reminder of num="+reminder);
		
	}
}
