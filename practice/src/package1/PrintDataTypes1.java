package package1;

public class PrintDataTypes1 {
   public static void main(String[] args) {
	 int a = 1;
	 float b = 2.0f;
	 double c = 6.097564;
	 char d = 'S';
	 long e = 668364;
	 short f = 687;
	 boolean g = false;
	 
	 System.out.println("value of int: "+a);
	 System.out.println("value of float: "+b);
	 System.out.println("value of double: "+c);
	 System.out.println("value of char: "+d);
	 System.out.println("value of long: "+e);
	 System.out.println("value of short: "+f);
	 System.out.println("value of boolean: " +g);
}
}
