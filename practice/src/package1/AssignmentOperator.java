package package1;

public class AssignmentOperator {
    public static void main(String[] args) {
		int x = 2;
		
		//Increment Operator
		x++;
		System.out.println(x);
		x++;
		System.out.println(x);
		x++;
		System.out.println(x);
		
		
		//Decrement Operator
		int y = 5;
		y--;
		System.out.println(y);
		
		y--;
		System.out.println(y);
		
		y--;
		System.out.println(y);
		
		
	}
    
}
