package package1;

public class PostIncrement {
   public static void main(String[] args) {
	int p = 6;
	
	    //Post increment
		 p = p++;
		 System.out.println("This is post increment: "+p);
		 
		 //Pre increment
		  p = ++p;
		  System.out.println("This is pre increment: "+p);
		  
		  //post decrement 
		  p = p--;
		  System.out.println( "This is post decrement: "+p);
		  
		  //pre decrement
		  p = --p;
		  System.out.println( "This is pre increment: "+p);
	}
	}
