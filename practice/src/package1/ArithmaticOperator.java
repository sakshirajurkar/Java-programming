package package1;

public class ArithmaticOperator {
   public static void main(String[] args) {
	int a = 5;
	int b = 4;

	int sum = a+b;
	System.out.println(sum);
	int substract = a-b;
	System.out.println(substract);
	int multiply = a*b;
	System.out.println(multiply);
	float division = a/b;
	System.out.println(division);
	float reminder = a%b;
	System.out.println(reminder);
	
	
}
}
