package package1;

public class AreaOfTriangleAndPerimeter {
 public static void main(String[] args) {
	 int base = 3;
	 int height = 4;
	 
	 //area of triangle
	 int area = (base*height)/2;
	 System.out.println("area of triangle:"+area);
	 
	 //perimeter of triangle
	 int a = 1;
	 int b =2;
	 int c = 3;
	 int p = a+b+c;
	 System.out.println("perimeter of triangle:"+p);
	 
	 
}
}
